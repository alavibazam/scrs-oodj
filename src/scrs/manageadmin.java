    private void btnreadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnreadActionPerformed
    if(flag){
    BufferedReader br = null;
    try{
        br = new BufferedReader(new FileReader("C://Users//Windwos 8.1//Documents//NetBeansProjects//Admin//src/login.txt"));
        
        //int val = Integer.parseInt(br.readLine());
        String ss;
        while((ss=br.readLine())!=null){
           // String ss = br.readLine();
            listModel.addElement(ss);
            
        }
        
      jList1.setModel(listModel); 
      flag = false;
    }catch(Exception e){
        System.out.println(""+e);
    }finally{
        try{
            br.close();
        }catch(Exception e){
            System.out.println(""+e);
        }
        
    }
    }
    }//GEN-LAST:event_btnreadActionPerformed

    private void updatebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebtnActionPerformed
        // TODO add your handling code here:
        String str = txtfield.getText();
        if(str.equals("")||str.equalsIgnoreCase("Select Data")){
            txtfield.setText("Select Data");
        }else{
         listModel.removeElementAt(index);
         listModel.add(index, str);
         jList1.setModel(listModel);
        }
    }//GEN-LAST:event_updatebtnActionPerformed

