    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        String str = txtfield.getText();
        if(str.equals("")||str.equalsIgnoreCase("Type Here"))
        {
            txtfield.setText("Type Here");
        }else{
          listModel.addElement(str);
          jList1.setModel(listModel);
          txtfield.setText("");
          flag = true;
        }        
    }//GEN-LAST:event_btnaddActionPerformed

    private void btnwriteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnwriteActionPerformed
        // TODO add your handling code here:
        int val = jList1.getModel().getSize();
        PrintWriter writer = null;
        try{
            writer = new PrintWriter("C://Users//Windwos 8.1//Documents//NetBeansProjects//Admin//src/login.txt");
           // writer.println(val);
            for(int i=0;i<val;i++){
                writer.println(jList1.getModel().getElementAt(i));
            }
        }catch(Exception e){
            System.out.println(""+e);
        }finally{
           writer.close();
        }
        
    }//GEN-LAST:event_btnwriteActionPerformed

