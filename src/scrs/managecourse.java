    private void btnwriteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnwriteActionPerformed
        // TODO add your handling code here:
        String filePath = "C://Users//Windwos 8.1//Documents//NetBeansProjects//Admin//src/course.txt";
        File file = new File(filePath);
        try {
            FileWriter fw = new FileWriter(file);
            BufferedWriter bw = new BufferedWriter(fw);
           
            
            for(int i = 0; i < jTable1.getRowCount(); i++){
                for(int j = 0; j < jTable1.getColumnCount(); j++){
                bw.write(jTable1.getValueAt(i, j).toString()+" ");
                
                }
                bw.newLine();
        }
        
        bw.close();
        fw.close();
        
        } catch (IOException ex) {
            Logger.getLogger(managecourse.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnwriteActionPerformed

    private void btnreadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnreadActionPerformed
        // TODO add your handling code here:
         String filePath = "C://Users//Windwos 8.1//Documents//NetBeansProjects//Admin//src/course.txt";
        File file = new File(filePath);
        
        try {
            FileReader fr = new FileReader(file);
            BufferedReader br = new BufferedReader(fr);
            
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            Object[] lines = br.lines().toArray();
            
            for(int i = 0; i < lines.length; i++){
                String[] row = lines[i].toString().split(" ");
                model.addRow(row);
            }
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(managecourse.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnreadActionPerformed

