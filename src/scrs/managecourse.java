    private void btnsave1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsave1ActionPerformed
        // TODO add your handling code here:
    String cnumber = txtnum.getText();
    String cname = txtname.getText();
    String duration = cbox;
    String level = cbox1;
    String fee = txtfee.getText();
    
    if(cnumber.equals("")||cname.equals("")||fee.equals("")){
        JOptionPane.showMessageDialog(rootPane, "Fill All Details", "Some Data is Missing", 0);
    }else{
    
    dtm = (DefaultTableModel) jTable1.getModel();
    dtm.addRow(new Object[]{cnumber,cname,duration,level,fee});
    jTable1.setModel(dtm);
    clearData();
    }
    }//GEN-LAST:event_btnsave1ActionPerformed

